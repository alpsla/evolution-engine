"""
Project initialization for Evolution Engine.

Detects environment (Git repo, CI provider, etc.) and helps users
set up the right integration path:
  1. CLI only — explore manually with evo analyze / evo report
  2. Automated hooks — auto-analyze on commit/push
  3. GitHub Action / GitLab CI — CI-driven analysis with PR/MR comments
  4. All of the above

Usage:
    from evolution.init import ProjectInit
    pi = ProjectInit(repo_path=".")
    env = pi.detect_environment()
    pi.setup(path="hooks")
"""

